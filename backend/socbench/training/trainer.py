"""Training loop — NanoGPT on Kaggle 2x T4 with DDP.

This module generates the training script that runs inside Kaggle notebooks.
"""

from __future__ import annotations

from pathlib import Path

from socbench.training.config import MODEL, TRAIN, KAGGLE


def generate_train_script(
    dataset_bin_path: str,
    output_dir: str = "/kaggle/working",
    tokens: int = 1_000_000_000,
) -> str:
    """Generate a standalone Python training script for Kaggle.

    This script is self-contained and runs with:
        torchrun --nproc_per_node=2 train.py
    """
    return f'''
"""
Socbench Training Script — GPT-2 124M on Kaggle 2x T4
Auto-generated by socbench. Do not edit manually.
"""
import os
import sys
import json
import time
import math
import pickle
from contextlib import nullcontext

import numpy as np
import torch
from torch.nn.parallel import DistributedDataParallel as DDP
from torch.distributed import init_process_group, destroy_process_group

# ── Config ──────────────────────────────────────────────────────────────────

dataset_path = "{dataset_bin_path}"
out_dir = "{output_dir}"
eval_interval = {TRAIN.eval_interval}
log_interval = {TRAIN.log_interval}
eval_iters = {TRAIN.eval_iters}
eval_only = False
always_save_checkpoint = True
init_from = "scratch"

# GPT-2 124M
n_layer = {MODEL.n_layer}
n_head = {MODEL.n_head}
n_embd = {MODEL.n_embd}
block_size = {MODEL.block_size}
vocab_size = {MODEL.vocab_size}
dropout = {MODEL.dropout}
bias = {MODEL.bias}

# Optimizer
learning_rate = {TRAIN.learning_rate}
max_iters = {tokens // (TRAIN.batch_size * 2 * TRAIN.gradient_accumulation_steps)}
weight_decay = {TRAIN.weight_decay}
beta1 = {TRAIN.betas[0]}
beta2 = {TRAIN.betas[1]}
grad_clip = {TRAIN.grad_clip}

# Schedule
warmup_iters = {TRAIN.warmup_tokens // (TRAIN.batch_size * TRAIN.gradient_accumulation_steps)}
lr_decay_iters = max_iters
min_lr = {TRAIN.lr_decay_to}

# System
device = "cuda"
dtype = "float16"
compile = {str(TRAIN.compile)}

# ── DDP Setup ───────────────────────────────────────────────────────────────

ddp = int(os.environ.get("RANK", -1)) != -1
if ddp:
    init_process_group(backend="nccl")
    ddp_rank = int(os.environ["RANK"])
    ddp_local_rank = int(os.environ["LOCAL_RANK"])
    ddp_world_size = int(os.environ["WORLD_SIZE"])
    device = f"cuda:{{ddp_local_rank}}"
    torch.cuda.set_device(device)
    master_process = ddp_rank == 0
    seed_offset = ddp_rank
    assert {TRAIN.gradient_accumulation_steps} % ddp_world_size == 0
    gradient_accumulation_steps = {TRAIN.gradient_accumulation_steps} // ddp_world_size
else:
    master_process = True
    seed_offset = 0
    gradient_accumulation_steps = {TRAIN.gradient_accumulation_steps}

if master_process:
    os.makedirs(out_dir, exist_ok=True)

torch.manual_seed(1337 + seed_offset)
torch.backends.cuda.matmul.allow_tf32 = True
torch.backends.cudnn.allow_tf32 = True
device_type = "cuda" if "cuda" in device else "cpu"
ptdtype = {"float32": torch.float32, "bfloat16": torch.bfloat16, "float16": torch.float16}[dtype]
ctx = nullcontext() if device_type == "cpu" else torch.amp.autocast(device_type=device_type, dtype=ptdtype)

# ── Data Loading ────────────────────────────────────────────────────────────

data = np.memmap(dataset_path, dtype=np.uint16, mode="r")
n_total = len(data)
n_train = int(0.9 * n_total)
n_val = n_total - n_train
train_data = data[:n_train]
val_data = data[n_train:]

def get_batch(split):
    data = train_data if split == "train" else val_data
    ix = torch.randint(len(data) - block_size, (batch_size,))
    x = torch.stack([torch.from_numpy((data[i:i+block_size]).astype(np.int64)) for i in ix])
    y = torch.stack([torch.from_numpy((data[i+1:i+1+block_size]).astype(np.int64)) for i in ix])
    if device_type == "cuda":
        x, y = x.pin_memory().to(device, non_blocking=True), y.pin_memory().to(device, non_blocking=True)
    else:
        x, y = x.to(device), y.to(device)
    return x, y

# ── Model ───────────────────────────────────────────────────────────────────

class CausalSelfAttention(torch.nn.Module):
    def __init__(self, config):
        super().__init__()
        assert config.n_embd % config.n_head == 0
        self.c_attn = torch.nn.Linear(config.n_embd, 3 * config.n_embd)
        self.c_proj = torch.nn.Linear(config.n_embd, config.n_embd)
        self.attn_dropout = torch.nn.Dropout(config.dropout)
        self.resid_dropout = torch.nn.Dropout(config.dropout)
        self.n_head = config.n_head
        self.n_embd = config.n_embd
        self.dropout = config.dropout
        self.flash = hasattr(torch.nn.functional, "scaled_dot_product_attention")
        if not self.flash:
            self.register_buffer("bias", torch.tril(torch.ones(config.block_size, config.block_size))
                                        .view(1, 1, config.block_size, config.block_size))

    def forward(self, x):
        B, T, C = x.size()
        q, k, v = self.c_attn(x).split(self.n_embd, dim=2)
        k = k.view(B, T, self.n_head, C // self.n_head).transpose(1, 2)
        q = q.view(B, T, self.n_head, C // self.n_head).transpose(1, 2)
        v = v.view(B, T, self.n_head, C // self.n_head).transpose(1, 2)
        if self.flash:
            y = torch.nn.functional.scaled_dot_product_attention(q, k, v, attn_mask=None, dropout_p=self.dropout if self.training else 0, is_causal=True)
        else:
            att = (q @ k.transpose(-2, -1)) * (1.0 / math.sqrt(k.size(-1)))
            att = att.masked_fill(self.bias[:,:,:T,:T] == 0, float("-inf"))
            att = torch.nn.functional.softmax(att, dim=-1)
            att = self.attn_dropout(att)
            y = att @ v
        y = y.transpose(1, 2).contiguous().view(B, T, C)
        y = self.resid_dropout(self.c_proj(y))
        return y

class MLP(torch.nn.Module):
    def __init__(self, config):
        super().__init__()
        self.c_fc = torch.nn.Linear(config.n_embd, 4 * config.n_embd)
        self.gelu = torch.nn.GELU()
        self.c_proj = torch.nn.Linear(4 * config.n_embd, config.n_embd)
        self.dropout = torch.nn.Dropout(config.dropout)

    def forward(self, x):
        x = self.c_fc(x)
        x = self.gelu(x)
        x = self.c_proj(x)
        x = self.dropout(x)
        return x

class Block(torch.nn.Module):
    def __init__(self, config):
        super().__init__()
        self.ln_1 = torch.nn.LayerNorm(config.n_embd, bias=config.bias)
        self.attn = CausalSelfAttention(config)
        self.ln_2 = torch.nn.LayerNorm(config.n_embd, bias=config.bias)
        self.mlp = MLP(config)

    def forward(self, x):
        x = x + self.attn(self.ln_1(x))
        x = x + self.mlp(self.ln_2(x))
        return x

class GPTConfig:
    def __init__(self, **kwargs):
        for k, v in kwargs.items():
            setattr(self, k, v)

class GPT(torch.nn.Module):
    def __init__(self, config):
        super().__init__()
        self.transformer = torch.nn.ModuleDict(dict(
            wte=torch.nn.Embedding(config.vocab_size, config.n_embd),
            wpe=torch.nn.Embedding(config.block_size, config.n_embd),
            drop=torch.nn.Dropout(config.dropout),
            h=torch.nn.ModuleList([Block(config) for _ in range(config.n_layer)]),
            ln_f=torch.nn.LayerNorm(config.n_embd, bias=config.bias),
        ))
        self.lm_head = torch.nn.Linear(config.n_embd, config.vocab_size, bias=False)
        self.transformer.wte.weight = self.lm_head.weight
        self.apply(self._init_weights)

    def _init_weights(self, module):
        if isinstance(module, torch.nn.Linear):
            torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)
            if module.bias is not None:
                torch.nn.init.zeros_(module.bias)
        elif isinstance(module, torch.nn.Embedding):
            torch.nn.init.normal_(module.weight, mean=0.0, std=0.02)

    def forward(self, idx, targets=None):
        B, T = idx.size()
        assert T <= block_size, f"Sequence length {{T}} > block_size {{block_size}}"
        pos = torch.arange(0, T, dtype=torch.long, device=idx.device)
        tok_emb = self.transformer.wte(idx)
        pos_emb = self.transformer.wpe(pos)
        x = self.transformer.drop(tok_emb + pos_emb)
        for block in self.transformer.h:
            x = block(x)
        x = self.transformer.ln_f(x)
        if targets is not None:
            logits = self.lm_head(x)
            loss = torch.nn.functional.cross_entropy(logits.view(-1, logits.size(-1)), targets.view(-1), ignore_index=-1)
        else:
            logits = self.lm_head(x[:, [-1], :])
            loss = None
        return logits, loss

batch_size = {TRAIN.batch_size}
model_args = dict(n_layer=n_layer, n_head=n_head, n_embd=n_embd, block_size=block_size, vocab_size=vocab_size, dropout=dropout, bias=bias)
config = GPTConfig(**model_args)
model = GPT(config)
model.to(device)

num_params = sum(p.numel() for p in model.parameters())
if master_process:
    print(f"Model parameters: {{num_params:,}}")

if ddp:
    model = DDP(model, device_ids=[ddp_local_rank])

raw_model = model.module if ddp else model
optimizer = raw_model.configure_optimizers(weight_decay, learning_rate, (beta1, beta2), device_type)
if compile:
    model = torch.compile(model)

# ── Learning Rate Schedule ──────────────────────────────────────────────────

def get_lr(it):
    if it < warmup_iters:
        return learning_rate * it / warmup_iters
    if it > lr_decay_iters:
        return min_lr
    decay_ratio = (it - warmup_iters) / (lr_decay_iters - warmup_iters)
    coeff = 0.5 * (1.0 + math.cos(math.pi * decay_ratio))
    return min_lr + coeff * (learning_rate - min_lr)

# ── Training Loop ───────────────────────────────────────────────────────────

X, Y = get_batch("train")
t0 = time.time()
local_iter_num = 0
running_mfu = -1.0
iter_num = 0
best_val_loss = 1e9
losses_log = []

if master_process:
    print(f"Starting training: {{max_iters}} iterations, {{tokens}} tokens")
    print(f"Dataset: {{dataset_path}}")
    print(f"Effective batch size: {{batch_size * gradient_accumulation_steps * (2 if ddp else 1)}}")

while iter_num < max_iters:
    lr = get_lr(iter_num)
    for param_group in optimizer.param_groups:
        param_group["lr"] = lr

    if iter_num % eval_interval == 0 and master_process:
        losses = torch.zeros(eval_iters)
        model.eval()
        for k in range(eval_iters):
            X_eval, Y_eval = get_batch("val")
            with ctx:
                _, loss = model(X_eval, Y_eval)
            losses[k] = loss.item()
        model.train()
        val_loss = losses.mean().item()
        losses_log.append(val_loss)
        if master_process:
            print(f"iter {{iter_num}}: val loss {{val_loss:.4f}}")
            if val_loss < best_val_loss:
                best_val_loss = val_loss
                checkpoint = {{
                    "model": raw_model.state_dict(),
                    "optimizer": optimizer.state_dict(),
                    "model_args": model_args,
                    "iter_num": iter_num,
                    "best_val_loss": best_val_loss,
                    "config": model_args,
                    "losses_log": losses_log,
                }}
                torch.save(checkpoint, os.path.join(out_dir, "ckpt.pt"))

    model.train()
    optimizer.zero_grad(set_to_none=True)

    for micro_step in range(gradient_accumulation_steps):
        if ddp:
            model.require_backward_grad_sync = (micro_step == gradient_accumulation_steps - 1)
        with ctx:
            logits, loss = model(X, Y)
            loss = loss / gradient_accumulation_steps
        loss.backward()

    torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip)
    optimizer.step()

    t1 = time.time()
    dt = t1 - t0
    t0 = t1
    if iter_num % log_interval == 0 and master_process:
        mfu = -1.0
        if running_mfu > 0:
            mfu = running_mfu
        tokens_per_sec = batch_size * block_size * gradient_accumulation_steps / dt if dt > 0 else 0
        print(f"iter {{iter_num}}: loss {{loss.item():.4f}}, lr {{lr:.6e}}, {{tokens_per_sec:.0f}} tok/s, mfu {{mfu:.2f}}")

    iter_num += 1
    local_iter_num += 1
    X, Y = get_batch("train")

# Save final
if master_process:
    checkpoint = {{
        "model": raw_model.state_dict(),
        "optimizer": optimizer.state_dict(),
        "model_args": model_args,
        "iter_num": iter_num,
        "best_val_loss": best_val_loss,
        "config": model_args,
        "losses_log": losses_log,
    }}
    torch.save(checkpoint, os.path.join(out_dir, "ckpt_final.pt"))

    # Save loss curve
    with open(os.path.join(out_dir, "loss_curve.json"), "w") as f:
        json.dump({{
            "losses": losses_log,
            "best_val_loss": best_val_loss,
            "total_tokens": tokens,
            "total_iters": iter_num,
        }}, f, indent=2)

    print(f"Training complete. Best val loss: {{best_val_loss:.4f}}")
    print(f"Loss curve saved to {{out_dir}}/loss_curve.json")

if ddp:
    destroy_process_group()
'''


def save_training_script(output_path: str, **kwargs) -> str:
    """Write the training script to a file."""
    script = generate_train_script(**kwargs)
    path = Path(output_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(script, encoding="utf-8")
    return str(path)
