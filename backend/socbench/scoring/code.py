"""Code-specific scorer — parse success, complexity, boilerplate detection."""

from __future__ import annotations

import ast
import math
from collections import Counter

from socbench.scoring.base import ScoreResult


def _tree_depth(node: ast.AST) -> int:
    if not list(ast.iter_child_nodes(node)):
        return 1
    return 1 + max(_tree_depth(child) for child in ast.iter_child_nodes(node))


def _cyclomatic_complexity(tree: ast.AST) -> int:
    complexity = 1
    for node in ast.walk(tree):
        if isinstance(node, (ast.If, ast.For, ast.While, ast.ExceptHandler)):
            complexity += 1
        elif isinstance(node, ast.BoolOp):
            complexity += len(node.values) - 1
    return complexity


async def code_scorer(samples: list[dict], text_key: str = "text") -> ScoreResult:
    """Analyze code quality: parseability, complexity, boilerplate."""
    texts = [s.get(text_key, "") for s in samples if s.get(text_key)]
    if not texts:
        return ScoreResult(name="code", score=0.0, warnings=["No text content"])

    total = len(texts)
    parseable = 0
    complexities: list[int] = []
    depths: list[int] = []
    loc_counts: list[int] = []
    boilerplate_count = 0

    # Common boilerplate patterns
    boilerplate_patterns = [
        "# This code is auto-generated",
        "# Generated by",
        "# DO NOT EDIT",
        "#!/usr/bin/env python",
        "# -*- coding:",
        '"""This module"',
        "'''This module'",
    ]

    for text in texts:
        lines = text.strip().split("\n")
        loc_counts.append(len(lines))

        # Check boilerplate
        if any(p in text[:200] for p in boilerplate_patterns):
            boilerplate_count += 1

        try:
            tree = ast.parse(text)
            parseable += 1

            # Complexity analysis
            cc = _cyclomatic_complexity(tree)
            complexities.append(cc)

            # Depth analysis
            d = _tree_depth(tree)
            depths.append(d)
        except SyntaxError:
            pass

    parse_rate = parseable / total if total > 0 else 0.0
    boilerplate_rate = boilerplate_count / total if total > 0 else 0.0

    # Complexity health: median between 2-10 is ideal
    median_cc = sorted(complexities)[len(complexities) // 2] if complexities else 1
    cc_health = 1.0 - abs(median_cc - 6) / 10
    cc_health = max(0.0, min(1.0, cc_health))

    # LOC health: median between 10-200 is ideal
    median_loc = sorted(loc_counts)[len(loc_counts) // 2] if loc_counts else 50
    loc_health = 1.0 - abs(math.log2(max(median_loc, 1)) - math.log2(50)) / 5
    loc_health = max(0.0, min(1.0, loc_health))

    # Score
    score = (
        0.40 * parse_rate
        + 0.20 * (1.0 - boilerplate_rate)
        + 0.20 * cc_health
        + 0.20 * loc_health
    )

    warnings = []
    if parse_rate < 0.8:
        warnings.append(f"Low parse success: {parse_rate:.1%}")
    if boilerplate_rate > 0.1:
        warnings.append(f"High boilerplate rate: {boilerplate_rate:.1%}")

    return ScoreResult(
        name="code",
        score=score,
        details={
            "parse_rate": round(parse_rate, 4),
            "boilerplate_rate": round(boilerplate_rate, 4),
            "median_complexity": median_cc,
            "median_loc": median_loc,
            "cc_health": round(cc_health, 4),
            "loc_health": round(loc_health, 4),
            "total_samples": total,
            "parseable": parseable,
        },
        warnings=warnings,
    )
